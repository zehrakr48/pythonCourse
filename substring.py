# Define the message
message = "python is awesome!"

# Print the characters from index 2 to 6 (inclusive of 2, exclusive of 6)
print(message[2:6])

# Print everything after index 10 (inclusive of 10)
print(message[10:])

# Print everything up to index 6 (exclusive of 6)
print(message[:6])

# In Python, character indexing starts at 0
